//package com.company;
public class Literals
{
    public static void main(String[] args)
    {
        byte age=34; //34 is integer literal
        char c='A'; //'A' is character literal
        float f=5.6f; //5.6f is floating literal as we've written 5.6f here otherwise it can be float or double
        double d=4.66; //default is double or we can write //double d=4.66d; //double d=4.66D; both
        System.out.println(age);
        //in java there are different classes for different primitive data types
    }
}